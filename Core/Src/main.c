/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 5 */

  InitUserConfig();
  SAOPortInit(0, &hi2c1, &huart1);
  SAOPortInit(1, &hi2c2, &huart2);

  /* Infinite loop */
  for(;;)
  {

      if (GetConfig()->ledMode == LED_MODE_ON) {
          HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
      }
      else if (GetConfig()->ledMode == LED_MODE_OFF) {
          HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);
      }
      else if (GetConfig()->ledMode == LED_MODE_BLINK) {
          HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
          osDelay(500);
          HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);
          osDelay(500);
      }
      taskYIELD();
  }
  /* USER CODE END 5 */
}

/* USER CODE BEGIN Header_StartCommsTask */
/**
* @brief Function implementing the commsTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartCommsTask */
void StartCommsTask(void *argument)
{
  /* USER CODE BEGIN StartCommsTask */
  /* Infinite loop */
  for(;;)
  {
      xSemaphoreTake(outputBinarySemHandle, portMAX_DELAY);
      if (xStreamBufferIsEmpty(stdoutBufferHandle) == pdFALSE) {
          size_t len;
          // Read up to len bytes from the buffer
          if ((len = xStreamBufferReceive(stdoutBufferHandle, outputBuffer, OUTPUT_BUFF_LEN, portMAX_DELAY)) > 0)
          {
              CDC_Transmit_FS((uint8_t *)outputBuffer, len);
          }
      }
      xSemaphoreGive(outputBinarySemHandle);
      taskYIELD();
  }
  /* USER CODE END StartCommsTask */
}


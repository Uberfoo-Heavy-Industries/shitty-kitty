void StartMainTask(void *argument)
{
  /* USER CODE BEGIN StartMainTask */
  /* Infinite loop */
  readline("> ");
  char * line = "";
  menu_func_t func = NULL;
  for(;;)
  {
      while (func == NULL) {
          displayMenu(current_menu);
          while (strlen(line = readline("> ")) <= 0) {
              displayMenu(current_menu);
          }
          func = findMenuFunc(line, current_menu);
      }
      func();
      func = NULL;
  }
  /* USER CODE END StartMainTask */
}

